#include <unistd.h>

void	ft_is_negative(int n)
{
	char	neg;
	char	p;

	neg = 78;
	p = 80;
	if (n < 0)
	{
		write(1, &neg, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}
