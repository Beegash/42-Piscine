int	ft_is_prime(int nb)
{
	int	index;

	index = 2;
	if (nb < 0)
		return (0);
	if (nb == 1)
		return (0);
	while (nb / index != 1)
	{
		if (nb % index == 0)
			return (0);
		index++;
	}
	return (1);
}
