int	ft_atoi(char *str)
{
	int	i;
	int	minus;
	int	num;

	num = 0;
	i = 0;
	minus = 1;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == 32)
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		num = num * 10 + (str[i] - 48);
		i++;
	}
	if (minus < 0)
		num *= -1;
	return (num);
}
